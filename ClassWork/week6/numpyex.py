import numpy as np

my_array = np.array([1, 2, 3, 4, 5])
print("My array is:", my_array)  # This will print the array [1 2 3 4 5]

print("Array * 2 is:", my_array * 2)  # This will print the array [2 4 6 8 10]
print("mean is:", np.mean(my_array))  # This will print the mean of the array, which is 3.0
print("square root is:", np.sqrt(my_array))  # This will print the square root of each element in the array